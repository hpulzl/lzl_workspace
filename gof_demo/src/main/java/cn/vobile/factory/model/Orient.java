package cn.vobile.factory.model;

public class Orient {

    String orienttype;
    int rangetype;
    String orientids;

    public String getOrienttype() {
        return orienttype;
    }

    public void setOrienttype(String orienttype) {
        this.orienttype = orienttype;
    }

    public int getRangetype() {
        return rangetype;
    }

    public void setRangetype(int rangetype) {
        this.rangetype = rangetype;
    }

    public String getOrientids() {
        return orientids;
    }

    public void setOrientids(String orientids) {
        this.orientids = orientids;
    }
}
